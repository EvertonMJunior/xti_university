package me.everton.logica;
/**
 * 
 * Operadores de Comparação (==, !=, >, <, >=, <=, instanceof).
 * 
 * @author Everton Marcelino Junior
 *
 */
public class OperadoresDeComparacao {
	public static void main(String[] args) {
		Integer x = 6;
		System.out.println("Oi" instanceof String ? "Sim" : "Não");
		System.out.println(x == 7 ? "Sim" : "Não");
	}
}
